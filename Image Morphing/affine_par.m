function [par] = affine_par(p1, p2, p3 , q1 , q2 , q3)
x = [p1(1,1) p1(1,2) 0 0 1 0;
     0 0 p1(1,1) p1(1,2) 0 1;
     p2(1,1) p2(1,2) 0 0 1 0;
     0 0 p2(1,1) p2(1,2) 0 1;
     p3(1,1) p3(1,2) 0 0 1 0;
     0 0 p3(1,1) p3(1,2) 0 1;
    ];

b = [ q1(1,1) q1(1,2) q2(1,1) q2(1,2) q3(1,1) q3(1,2) ]';
 
par = x\b;

end